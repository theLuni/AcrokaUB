#!/bin/bash

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–≤–æ–¥–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –æ–± –æ—à–∏–±–∫–µ –∏ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è
error_exit() {
