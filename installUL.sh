        return 0
    fi
    
    if git clone https://github.com/theLuni/AcrokaUB.git; then
        echo "‚úÖ –†–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π —É—Å–ø–µ—à–Ω–æ –∫–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω"
        return 0
    fi
